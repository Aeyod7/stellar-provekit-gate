fn fmt_u8_array(name: &str, bytes: &[u8; 64]) -> String {
    let inner: Vec<String> = bytes.iter().map(|b| format!("0x{b:02x}")).collect();
    let lines: Vec<String> = inner
        .chunks(8)
        .map(|c| c.join(", "))
        .collect();
    format!(
        "const {name}: [u8; 64] = [\n    {}\n];",
        lines.join(",\n    ")
    )
}

fn fmt_u8_array_g2(name: &str, bytes: &[u8; 128]) -> String {
    let inner: Vec<String> = bytes.iter().map(|b| format!("0x{b:02x}")).collect();
    let lines: Vec<String> = inner
        .chunks(8)
        .map(|c| c.join(", "))
        .collect();
    format!(
        "const {name}: [u8; 128] = [\n    {}\n];",
        lines.join(",\n    ")
    )
}

fn vk_g1(x: &str, y: &str) -> Result<G1Affine> {
    let p05 = risc0_g1_from_limbs(x, y)?;
    let mut buf = vec![];
    p05.serialize_uncompressed(&mut buf).unwrap();
    g1_from_05_uncompressed(&buf)
}

fn vk_g2(x1: &str, x2: &str, y1: &str, y2: &str) -> Result<G2Affine> {
    let p05 = risc0_g2_from_sol_const(x1, x2, y1, y2)?;
    let mut buf = vec![];
    p05.serialize_uncompressed(&mut buf).unwrap();
    g2_from_05_uncompressed(&buf)
}

